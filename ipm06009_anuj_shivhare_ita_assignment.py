for name, model in classifiers.items():
    print(f"\n  Training {name}...")
    model.fit(X_train, yc_train)
    y_pred = model.predict(X_test)

    acc  = accuracy_score(yc_test, y_pred)
    prec = precision_score(yc_test, y_pred, average='weighted', zero_division=0)
    rec  = recall_score(yc_test, y_pred, average='weighted', zero_division=0)
    f1   = f1_score(yc_test, y_pred, average='weighted', zero_division=0)
    cv   = cross_val_score(model, X_scaled, y_class, cv=5, scoring='accuracy').mean()

    clf_results[name] = {'Accuracy': acc, 'Precision': prec,
                         'Recall': rec, 'F1 Score': f1, 'CV Score': cv}
    clf_models[name]  = (model, y_pred)

    print(f"    ✅ Accuracy: {acc:.4f} | F1: {f1:.4f} | CV: {cv:.4f}")

