def predict_new_case(case_type, case_category, court_level, state,
                     year_filed, num_hearings, num_adjournments,
                     num_parties, judge_changes, case_status):
    """
    Predict Priority Level and Case Duration for a new case.
    """
    # Encode inputs
    def safe_encode(le, val):
        if val in le.classes_:
            return le.transform([val])[0]
        return 0  # default if unseen

    ct_enc  = safe_encode(le_dict['Case_Type'],      case_type)
    cc_enc  = safe_encode(le_dict['Case_Category'],  case_category)
    cl_enc  = safe_encode(le_dict['Court_Level'],    court_level)
    st_enc  = safe_encode(le_dict['State'],          state)
    cs_enc  = safe_encode(le_dict['Case_Status'],    case_status)

    # Derived features
    adj_rate   = num_adjournments / (num_hearings + 1)
    hear_ratio = num_hearings / 25  # normalizing proxy
    is_crim    = 1 if case_type == 'Criminal' else 0
    is_pend    = 1 if case_status == 'Pending' else 0
    age_est    = min((2024 - year_filed) * 12, 240)
    age_cat    = 0 if age_est <= 12 else (1 if age_est <= 36 else (2 if age_est <= 72 else 3))

    input_vec = np.array([[ct_enc, cc_enc, cl_enc, st_enc, year_filed,
                           num_hearings, num_adjournments, num_parties,
                           judge_changes, is_crim, is_pend,
                           adj_rate, hear_ratio, age_cat]])
    input_scaled = scaler.transform(input_vec)

    # Predictions
    best_clf_model = clf_models[best_clf][0]
    priority_enc   = best_clf_model.predict(input_scaled)[0]
    priority_label = le_dict['Priority_Level'].inverse_transform([priority_enc])[0]

    best_reg_model = reg_models[best_reg][0]
    duration_pred  = max(1, best_reg_model.predict(input_scaled)[0])

    return priority_label, round(duration_pred, 1)


