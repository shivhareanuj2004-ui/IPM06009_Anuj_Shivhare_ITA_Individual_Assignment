df_ml = df.copy()

# --- New derived features ---
df_ml['Adjournment_Rate']   = df_ml['Number_of_Adjournments'] / (df_ml['Number_of_Hearings'] + 1)
df_ml['Hearing_Age_Ratio']  = df_ml['Number_of_Hearings'] / (df_ml['Case_Age_Months'] + 1)
df_ml['Is_Criminal']        = (df_ml['Case_Type'] == 'Criminal').astype(int)
df_ml['Is_Pending']         = (df_ml['Case_Status'] == 'Pending').astype(int)
df_ml['Age_Category']       = pd.cut(df_ml['Case_Age_Months'],
                                      bins=[0, 12, 36, 72, 1000],
                                      labels=[0, 1, 2, 3]).astype(int)

print("✅ New features created:")
print("   • Adjournment_Rate   = adjournments / (hearings + 1)")
print("   • Hearing_Age_Ratio  = hearings / (case_age + 1)")
print("   • Is_Criminal        = 1 if Criminal, else 0")
print("   • Is_Pending         = 1 if Pending, else 0")
print("   • Age_Category       = Binned case age (0-3)")

# --- Encode categorical columns ---
le_dict = {}
cat_cols = ['Case_Type', 'Case_Category', 'Court_Level', 'State', 'Case_Status', 'Priority_Level']
for col in cat_cols:
    le = LabelEncoder()
    df_ml[col + '_enc'] = le.fit_transform(df_ml[col])
    le_dict[col] = le

print("\n✅ Label encoding done for:", cat_cols)

